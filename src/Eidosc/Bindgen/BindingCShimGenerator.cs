using System.Text;

namespace Eidosc.Bindgen;

public sealed class BindingCShimGenerator
{
    private readonly CHeaderIr _ir;
    private readonly BindingTypeMapper _typeMapper;

    public BindingCShimGenerator(CHeaderIr ir)
    {
        _ir = ir;
        _typeMapper = new BindingTypeMapper(ir);
    }

    public bool HasShims => _ir.Functions.Any(NeedsShim);

    public string Generate()
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated>");
        sb.AppendLine("// Generated by eidosc pkg bind. Do not edit this file directly.");
        sb.AppendLine("// </auto-generated>");
        sb.AppendLine($"#include \"{Path.GetFileName(_ir.Header)}\"");
        sb.AppendLine();

        foreach (var fn in _ir.Functions.Where(NeedsShim).OrderBy(static fn => fn.Name, StringComparer.Ordinal))
        {
            GenerateShim(sb, fn);
            sb.AppendLine();
        }

        return sb.ToString();
    }

    private bool NeedsShim(CBindingFunction fn) =>
        fn.Parameters.Any(parameter => _typeMapper.Map(parameter.Type).Category == BindingTypeCategory.StructByValue);

    private void GenerateShim(StringBuilder sb, CBindingFunction fn)
    {
        var returnMapping = _typeMapper.Map(fn.ReturnType);
        if (returnMapping.Category == BindingTypeCategory.StructByValue)
            throw new InvalidOperationException($"Function '{fn.Name}' returns struct by value; define an explicit pointer-returning C wrapper.");

        sb.Append($"{fn.ReturnType.Spelling} eidos_shim_{fn.Name}(");
        sb.Append(string.Join(", ", fn.Parameters.Select(FormatShimParameter)));
        sb.AppendLine(")");
        sb.AppendLine("{");
        sb.Append("    ");
        if (returnMapping.EidosType != "Unit")
            sb.Append("return ");
        sb.Append($"{fn.Name}(");
        sb.Append(string.Join(", ", fn.Parameters.Select(FormatShimArgument)));
        sb.AppendLine(");");
        sb.AppendLine("}");
    }

    private string FormatShimParameter(CBindingParameter parameter)
    {
        var mapping = _typeMapper.Map(parameter.Type);
        return mapping.Category == BindingTypeCategory.StructByValue
            ? $"void *{parameter.Name}_ptr"
            : $"{parameter.Type.Spelling} {parameter.Name}";
    }

    private string FormatShimArgument(CBindingParameter parameter)
    {
        var mapping = _typeMapper.Map(parameter.Type);
        return mapping.Category == BindingTypeCategory.StructByValue
            ? $"*({parameter.Type.Spelling}*){parameter.Name}_ptr"
            : parameter.Name;
    }
}
